import tkinter
from base import *
from blocks import *

class GD(Game):
    def __init__(self, root):
        self.hitbox = False # whether spikes have hitboxes
        self.over = False # if game is over
        self.speed = 3 # how fast player moves
        super().__init__(500, 500, root)
        self.moved = 0
        self.bind_all('<Key>', self.stop)
        self.init()
        self.after(1, self.update) # begin mainloop
    def stop(self, e):
        if e.keysym == 'e':
            self.speed = 0
    def init(self):
        Under(25, -50, self) # thing that blocks player from falling up (gd reference)
        Under(25, 401, self) # thing player rests on (invisible)
        self.create_rectangle(0, 350, 530, 530, fill = "blue") # ground (fake)
        self.create_rectangle(0, 0, 530, 351, fill = "light blue") # background
        self.player = Cube(25, 300, self, mode="wave")
        # actual level
        level = eval(open("level.txt", "r").read())
        for i in level: # makes everything
            if i[0] == 0:
                Spike(i[1], i[2], self)
            elif i[0] == 1:
                Block(i[1], i[2], self)
            elif i[0] == 2:
                Jump(i[1], i[2], self)
            elif i[0] == 3:
                Orb(i[1], i[2], self)
    def cust(self):
        self.moved += self.speed
    def restart(self):
        del self.player
        for i in self.objs:
            i.move(0, 0)
        self.player = Cube(25, 300, self, mode="ship")

if __name__ == "__main__":
    root = tkinter.Tk()
    root.title("Square Jump by Minglang")
    game = GD(root)
    root.mainloop()
